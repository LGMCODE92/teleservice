package domain;

public class Configuration {
	
	//atributos
	private String key;
	private String value;
	
	
	//getters y setters
	public String getKey() {
		return key;
	}
	public void setKey(String clave) {
		this.key = clave;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String valor) {
		this.value = valor;
	}
	
	
}
